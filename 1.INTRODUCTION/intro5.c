    // Program to add two integers.

    #include <stdio.h>

    int main()
    {
        int a;
        printf("Enter The First Number =  ");
        scanf("%d", &a);
        int b;
        printf("Enter The Second Number =  ");
        scanf("%d", &b);

        int sum;
        sum = a + b;
        printf("The sum of two numbers is = %d\n", sum);
        return 0;
    }